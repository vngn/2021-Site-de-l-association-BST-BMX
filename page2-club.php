    <section id="historique" class="id">
        <div class="bloc-partie fond-bloc">
            <h1 class="fond-titres-club historique h1white">Historique du club</h1>
            <hr>
            <p>Crée en 2014 sous l’impulsion de la mairie de Bailleul sur Thérain et la construction de la piste, le
                club BST BMX était initialement affilié au Team Oise organisation (club de vélo de route /vtt).</p>

            <p> Deux coachs sportifs avait été engagés dans l’association Bailleul sur Thérain BMX, Mr Nion et Mr
                Hansart en 2014.</p>

            <p>Suite à un détachement de Mr Hansart et du Team Oise Organisation, Mr Nion, avec l’aide des membres
                de
                l’association, continue à faire vivre le club avec la dispense des entrainements en sa qualité de
                coach
                sportif.</p>

            <p>La Mairie de Bailleul sur Thérain devient le principal partenaire de l’association.</p>

            <p>L’arrivée des cours collectifs du centre périscolaire de Bailleul sur Thérain, la récente création de
                la
                piste, la proximité avec la ville de Beauvais sont des éléments qui ont permis à l’association
                d’attirer
                de nouveaux adhérents de tout âge.</p>

            <p>En Octobre 2017, l’association Bailleul sur Thérain BMX change de nom et devient le BST BMX avec la
                nomination d’un nouveau président Monsieur Maurez.</p>

            <p>Graphiste dans l’entreprise Riderunik (à Bresles), ce fabricant de kits déco devient également le 1er
                sponsor du BST BMX en fournissant des maillots et des gants au club.</p>

            <p>Par la suite le magasin de cyclisme "Au vélo" situé à Breuil le Vert devient notre second sponsor mais
                aussi par la même occasion notre fournisseur de pièces pour BMX Race.</p>

            <p>Depuis 2020, l’entreprise LC Rénov menuiserie localisée à Abbecourt est devenu notre 3 ème
                partenaire.
            </p>

            <p>
                <img class="photo-page-club" src="images/butte.jpg" alt="photo de la butte">
            </p>

            <p>
                <img class="photo-page-club" src="images/pilote.jpg" alt="photo d'un pilote">
            </p>

            <p>Lors des années 2018-2019, Un changement de présidence : Monsieur Cochon a succédé à Monsieur Maurez
                puis
                lors de l’année 2019 une passation de pouvoir a été effectuée entre Monsieur Cochon et Monsieur
                Vandenhecke précédemment trésorier.</p>

            <p>Le 15 septembre 2019, le BST BMX a organisé avec succès sa première compétition de coupe régionale
                d’Automne BMX Hauts-de-France.</p>

            <p>Grâce au dévouement des membres bénévoles du club et un entretien irréprochable de la piste,
                l’accueil
                d’un grand nombre de pilotes de Hauts-de-France a été perçu comme très positif par les clubs
                accueillis.
            </p>

            <p>Des retombées économiques positives pour l’environnement local du club, sont apparues (camping de
                Bresles complet, boulangerie sollicitée).</p>

            <p>
                <img class="photo-page-club photo-affiche" src="images/affiche.jpg" alt="affiche">
            </p>

            <p>Depuis juillet 2020, création d’une nouvelle plage horaire de pratique (3 au total par semaine) :<br>
                • Le dimanche matin de 10h à 12h.
            </p>
